# ==========================================================
#  FINAL ULTRA-FAST PST ACCESSOR
# ==========================================================

@njit(int32(int32, int32, int32), fastmath=True)
def pst_eval(piece, square, endgame):
    # 0–5 = white, 6–11 = black
    is_black = piece >= 6
    p = piece - 6 if is_black else piece

    if endgame:
        return -PST_END_BLACK[p, square] if is_black else PST_END[p, square]
    else:
        return -PST_MID_BLACK[p, square] if is_black else PST_MID[p, square]
